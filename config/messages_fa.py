    @staticmethod
    def format_number(num: int) -> str:
        """Format number with K/M suffix for Persian display."""
        if num >= 1_000_000:
            return f"{num / 1_000_000:.1f}M"
        elif num >= 1_000:
            return f"{num / 1_000:.1f}K"
        return str(num)
    
